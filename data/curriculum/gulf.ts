const lesson = (
  unitId: string,
  contentId: string,
  title: string,
  commercialAccess: 'free' | 'premium',
  lessonWords?: CurriculumItem['lessonWords'],
  lessonKey?: CurriculumItem['lessonKey'],
  subtitle?: string,
): CurriculumItem => ({
  dialect,
  unitId,
  contentId,
  contentType: 'lesson',
  title,
  subtitle,
  route: { screen: 'lesson', params: { type: contentId } },
  homeHref: `/lesson?type=${contentId}`,
  availability: 'available',
  commercialAccess,
  lessonWords,
  lessonKey,
  acceptedTransliterationProfile: 'gulf',
});

const scenario = (
  unitId: string,
  contentId: string,
  title: string,
  scenarioName: string,
  commercialAccess: 'free' | 'premium',
  subtitle?: string,
): CurriculumItem => ({
  dialect,
  unitId,
  contentId,
  contentType: 'scenario',
  title,
  subtitle,
  route: { screen: 'scenario', params: { type: scenarioName } },
  homeHref: SCENARIO_HOME_HREFS[scenarioName] ?? `/scenario?type=${scenarioName}`,
  availability: 'available',
  commercialAccess,
  scenarioName,
  sceneImageKey: scenarioName,
  acceptedTransliterationProfile: 'gulf',
});

const quiz = (
  unitId: string,
  contentId: string,
  title: string,
  quizUnit: string | undefined,
  screen: 'quiz' | 'quiz-unit2',
  commercialAccess: 'free' | 'premium',
  subtitle?: string,
): CurriculumItem => ({
  dialect,
  unitId,
  contentId,
  contentType: 'quiz',
  title,
  subtitle,
  route: { screen, params: quizUnit ? { unit: quizUnit } : {} },
  homeHref: screen === 'quiz'
    ? (quizUnit ? `/quiz?unit=${quizUnit}` : '/quiz')
    : `/quiz-unit2?unit=${quizUnit}`,
  availability: 'available',
  commercialAccess,
  quizUnit,
  acceptedTransliterationProfile: 'gulf',
});

